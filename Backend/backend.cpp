// A struct that makes sending
// complex numbers back and forth
// between c++ and javascript easier.
struct complexReplacement {
  double re;
  double im;
};

// Create a class we can return to our jsx.
class NormClass {
  // Make all the values public
 public:
  double probZero;
  double probOne;
  double normAmpFactor;
  complexReplacement alphaStruct;
  complexReplacement betaStruct;

  // Make the consturctor
  NormClass(double pZero, double pOne, double normAmp, complexReplacement a,
            complexReplacement b) {
    probZero = pZero;
    probOne = pOne;
    normAmpFactor = normAmp;
    alphaStruct = a;
    betaStruct = b;
  }

  // Make a default construct to prevent enscripten from getting angry
  NormClass() = default;
};

class AlphaAndBetaClass {
 public:
  complexReplacement alpha;
  complexReplacement beta;

  // Make the constructor
  AlphaAndBetaClass(complexReplacement a, complexReplacement b) {
    alpha = a;
    beta = b;
  }

  // Make a default constructor for enscripten
  AlphaAndBetaClass() = default;
};

